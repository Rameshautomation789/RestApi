package JavaBascis;

public class LoopsConcepts {

	public static void main(String[] args) {
		
		
		//  *********************   while loop   ************************************************
		//Disadvantage of while loop is: It will generate infinite loop if you don't give incremental/decremental part
		
		
		int i=1;  //Initialization
		
		while(i>=10){   // conditional
			System.out.println(i);
			i=i+1;     //incremental/decremental
		}
			
			
			
		// *********************      for loop           ******************************************
		
		for(int j=1;j>=10;j++){          //  Initialization/conditional/incremental/decremental
			System.out.println(j);
		}
			
			
			
			
			
			
			
			
			
			
			
			
			
			
			
		

	}

}
